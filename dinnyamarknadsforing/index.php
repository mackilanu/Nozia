NOZIA är ett nytt marknadsföringsverktyg på marknaden som kommer ändra hur både företagare och konsumenter ser på reklam.<br><br>

Utmaningen ligger i att nå ut till konsumenterna på ett sätt som gör dem nöjda med det dem får.<br><br>

Ingen vill se reklam i TV:en under sitt favoritprogram eller höra den när dom lyssnar på musik via radion.<br><br>

Så vi på NOZIA har klurat och funderat på: <b>”Hur kan vi skapa en situation där konsumenterna är ok med att ta emot reklam?”</b><br><br>

För gemene man vill ha reklam, men dem vill själva bestämma när var och hur dem tar del av reklamen.<br><br>

Så vi skapade sidan/appen NOZIA, Sverige första kommersiella media.<br><br>

Du skapar ett erbjudande och konsumenterna inom det segment du har valt får reda på det direkt, men väljer själva när dom tar del av det.<br><br>

Marknadsföring handlar om att hitta ett behov, ge en lösning på behovet och leva upp till förväntningarna, det brukar gå bra så länge man är ensam om att sälja en lösning, men så fort det kommer konkurrens så är marknadsföring alltmer viktigt.<br><br>

Så vi tar en godisbutik som ett exempel till att börja med, vi befinner oss på ett köpcenter i Sverige och det finns 3 stycken butiker med godis med liknande utbud.<br><br>

Här måste man jobba hårt för att få konsumenterna att välja just din butik, man kan göra det genom att göra tidningsinlägg, meddelande på radion, genom högtalarsystemet på centret eller via din hemsida, där du är beroende av att konsumenterna navigerar in på sidan.<br><br>

Alla de här lösningarna är relativt dyra och tar tid att organisera.<br><br>

Med NOZIA kan du ligga före konkurrenterna och samtidigt hålla bra koll på hur dem marknadsför sig, ditt mål är att konsumenterna ska komma till dig och med NOZIA går du enkelt in på din sida, skapar ett erbjudande och skickar ut det.<br><br>

Har du en klädaffär med produkter för både män och kvinnor?<br><br>

Med tryckt reklam så måste du skicka ut samma material till alla hushållen vilket leder till stora mängder svinn.<br><br>

Med NOZIA kan du genom segmenteringen skapa unika erbjudanden för personer på olika platser, kön, och ålder.<br><br>

<b>Exempel:<br><br>

Trosor och B-H 10% rabatt mellan 14:00-15:00 nu på lördag den 22 April (Segment: Kvinnor mellan 18 och 29 i Karlstad kommun).<br><br>

Klänningar 2 för 1 hela helgen från den 21 April till den 23 April (Segment: Kvinnor mellan 35 och 50 i Örebro kommun). <br><br> 

Kavaj och skjorta endast 595:- på fars dag (Segment: Män i alla åldrar i Örebro, Karlstad och Karlskoga kommun).<br><br>
</b>
Du kan skapa ett erbjudande på bikinis eller badbyxor med olika design för olika åldrar och kön i de kommuner du känner att det är rimligt att dem sätter sig i bilen och kommer till dig.<br><br>

Det tar ingen tid, du behöver inte tänka på att det är en kostnad, för den har du redan tagit hand om för ett helt år, du kan komma på en kampanj på kvällen innan och på morgonen när all vaknar så har konsumenterna tagit del av det innan den tryckta reklamen ens hamnat i brevlådan, du kan sitta på lunch och tänka, nu ska jag bli av med de där 2 TV apparaterna för att få plats med nya modeller, jag ska ha en tipspromenad i butiken och de 2 med bäst resultat, får köpa en TV var för halva priset samtidigt som tipspromenaden lockar konsumenter till din butik vilket ger merförsäljning.<br><br>

Upplägget tar nästan ingen tid alls att skapa.<br><br>

