<b>Hur påverkar något så simpelt som pappersreklam vår miljö?</b><br>

Vi vet att reklamen är gjord av papper och att papper är tillverkat av träd, så hur ser egentligen ett träds liv ut som reklamformat?<br>

Om vi leker med tanken att ett träd symboliserar en stor pappersmassa, så ser den ut ungefär så här:<br>

Utanför Årjäng i Värmland växer skogen tät och det finns gott om markägare som sysslar med avverkning av skog för avkastning.<br>

Trädet sågas ner och förs därefter till en uppsamlingsplats för upphämtning.<br>

Därefter hämtar en lastbil upp trädet för att göra pappersmassa och tillverka pappret på pappersbruket i Skoghall<br>

När det färdiga pappret ligger på rullar skickas det vidare till köparen, ett tryckeri i låt oss säga Karlstad.<br>

Väl på plats så trycks diverse olika reklamtryck upp och trädet får för första gången sen det stod i skogen en färg igen, en färg fylld av kemikalier.<br>

Därefter så är det postleverantörernas tur att se till att trädet i form av pappersreklam kommer ut till människorna i de 25 största orterna i Värmland. (Går naturligtvis ut till samtigla i hela länet)<br>

I hemmen hamnar reklamen på köksbordet, vardagsrumsbordet och i sängkammaren.<br>

Därefter slängs det förhoppningsvis bland pappersåtervinningen för att sedan föras till den lokala soptippen för att återigen hämtas upp och föras till Stora Enso i Skoghall.<br>

Om vi ugår ifrån att en bit av vårat träd har varit en del av varje reklampapper som skickats ut till de 25 största orterna i Värmland så har trädet färdats 485,47 Mil.<br>

Reklamen hamnar på orterna främst med hjälp av postens lastbilar.<br>

En lastbil har en genomsnittsförbrukning på 4 Liter/Mil.<br>

För varje förbränd liter bränsle har vi ett CO² utsläpp på 2,28 kg<br>

Det vill säg att det förbrukas 1.941,88 liter bränsle vilket ger ett CO² utsläpp på 4.47 Ton.<br>

Ett träd eller pappersark kan återanvändas upp till 7 gånger innan fibrerna i materialet är slut och slutligen skickas för att brännas till energi.<br>

Vilket innebär att vi kan ta 485,47 Mil, 1.941,88 liter och 4,47 Ton och multiplicera det med 7, då får vi under trädets hela resa från skog till att brännas de här siffrorna:<br>

Mil: 3.398,29 Mil<br>
Bränsle: 13.593,16 Liter<br>
CO²: 30,992 Ton<br>
Om vi utgår ifrån att varje av Sveriges 25 landskap har samma siffror som i det här fallet, så skulle CO² utsläppet ligga på 774,8 Ton.<br>

Men vi kan inte utgå ifrån de siffrorna, alla län och landskap har olika förutsättningar när det kommer till både skog och pappersbruk, sträckor som körs blir längre och CO² utsläppen blir högre.<br>

Inräknat är inte heller alla utsläpp i samband med avverkningen och de postbilar som kör ut reklamen till folkets brevlådor.<br><br>

<b>Hur påverkar CO² vår miljö?</b><br>

CO² är en växthusgas som bidrar med att värma upp vår planet med hjälp av solens UV strålar.<br>

När UV strålarna träffar ytan på vår planet så studsar dom upp mot vår atmosfär och ut i rymden igen, medans värmen studsar från jordytan upp i atmosfären och ner igen, vilket är det som gör våran planet bebolig.<br>

De extra utsläppen av CO² bidrar i stor utsträckning till ökad växthuseffekt på planeten, som kan leda till extremväder, höjd havsnivå och en ny istid.<br>
